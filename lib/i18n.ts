type Lang = keyof typeof strings;
type StringKey = keyof typeof strings.tr;

function detectLang(): Lang {
  try {
    const locales = getLocales();
    const tag = (locales[0]?.languageTag || locales[0]?.languageCode || "en").toLowerCase();
    const code = tag.split("-")[0] as Lang;
    if (code in strings) return code;
  } catch {}
  return "en";
}

let _lang: Lang | null = null;

export function getLang(): Lang {
  if (!_lang) _lang = detectLang();
  return _lang;
}

export function t(key: StringKey): string {
  const lang = getLang();
  return (strings[lang] as any)[key] ?? (strings.en as any)[key] ?? key;
}

/** Force a specific language (e.g. from user profile) */
export function setLang(lang: string) {
  if (lang && lang in strings) _lang = lang as Lang;
}
